// switch case dart

void main()
{
    int number = 1;
    switch (number) {
    case 1: {
        print("number 1");
    } break;
    case 2: {
        print("number 2");
    } break;
    case 3: {
        print(" number 3");
    } break;
    default: {
        print("This is default case");
    } break;
    }
}
